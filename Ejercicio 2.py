# Solicitar el nombre completo del usuario
nombre_completo = input("Por favor, ingresa tu nombre completo: ")

# Mostrar el nombre completo en minúsculas
nombre_minusc = nombre_completo.lower()
print("Nombre completo en minúsculas:", nombre_minusc)

# Mostrar el nombre completo en mayúsculas
nombre_mayusc = nombre_completo.upper()
print("Nombre completo en mayúsculas:", nombre_mayusc)

# Mostrar el nombre completo con la primera letra en mayúscula
nombre_capitalizado = nombre_completo.title()
print("Nombre completo con la primera letra en mayúscula:", nombre_capitalizado)